#include<stdio.h>
void main()
{
	
	int i,j,rows,space;
	
	printf("Enter number of rows=");
	scanf("%i",&rows);
	 
	printf("\n");
	
	for(i=1;i<=rows;i++)
	{
		for(space=1;space=i-j;space--)
		{
			printf(" ");
		}
		
		for(j=1;j<=i;j++)
			
		{
			
			printf("*");
			
		}
		
		printf("\n");
	}
		
		
}
	
			

