#include<stdio.h>
void main()
{
	
	int i,j,rows,columns;
	printf("Enter number of Rows=");
	scanf("%i",&rows);
	
	printf("Enter number of Columns=");
	scanf("%i",&columns);
	printf("\n\n");
	
	for(i=1;i<=rows;i++)  // for rows
	{
		for(j=1;j<=columns;j++)  // for columns
		{
			printf("\t*");
		
		}
	
	printf("\n");
	
	}
	
	
	}
